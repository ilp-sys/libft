#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s1) + 1;
	copy = (char *)malloc(sizeof(char) * len);
	if (!copy)
		return (NULL);
	ft_memcpy(copy, s1, len);
	return (copy);
}
