int	ft_isalnum(int ch)
{
	int	flag;

	flag = 0;
	if (ch >= 'a' && ch <= 'z')
		flag = 1;
	else if (ch >= 'A' && ch <= 'Z')
		flag = 1;
	else if (ch >= '0' && ch <= '9')
		flag = 1;
	return (flag);
}
